var day=Math.random();
day=day*5;

if(day<1){
    day=day+1;
}
day=Math.floor(day);





switch(day){

case 1:
    document.write("Sat");
    break;
    case 2:
        document.write("Sun");
        break;
        case 3:
            document.write("Mon");
            break;
            case 4:
                document.write("Tue");
                break;

}


 
























